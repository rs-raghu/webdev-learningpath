# Initialize a new Git repository in the current directory
git init

# Check the status of the working directory and staging area
git status

# Add file(s) to the staging area, preparing for commit
git add filename.txt

# Show changes between working directory and staging area (unstaged changes)
git diff

# Commit staged changes permanently in the repository with a message
git commit -m "Describe the changes made"

# View the list of previous commits in the repository
git log
